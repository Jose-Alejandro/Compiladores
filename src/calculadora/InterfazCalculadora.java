    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        calculadora = new Calculadora( (String) jTextField1.getText() );
        boolean isOK = false;
        switch(radio){
            case 1: 
                
                isOK = calculadora.AnalizarExpr();
                
                if(isOK){
                    jTextField2.setText("" + calculadora.result);
                    jTextField3.setText(calculadora.prefijo);
                    jTextField4.setText(calculadora.posfijo);
                }else{
                    JOptionPane.showMessageDialog(this, "La cadena NO es v√°lida");
                    jTextField2.setText("" + calculadora.result);
                    jTextField3.setText("");
                    jTextField4.setText("");
                }
                break;
            case 2:
                AFN_Autom regul = new AFN_Autom( (String) jTextField1.getText() );
                regul.analizar();
                isOK = regul.result.AnalizarCadena( (String) jTextField5.getText() );
                //isOK = regul.result.AnalizarCadena( "ababzz" );
                
                System.out.println("Entradas:");
                System.out.println("Cadena: " + (String) jTextField1.getText());
                System.out.println("Entrada: " + (String) jTextField5.getText());
                System.out.println("Bool: " + isOK );
                
                if(isOK){
                    JOptionPane.showMessageDialog(this, "La cadena ES v√°lida");
                }else{
                    JOptionPane.showMessageDialog(this, "La cadena NO es v√°lida");
                }
                break;
            default: JOptionPane.showMessageDialog(this, "Error");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

